package Logical_patterns;

public class eg3 {

	public static void main(String[] args) {
//		*****
//		****
//		***
//		**
//		*

		for(int i=1; i<=5; i++)               // row
		{
			for(int j=i;  j<=5;  j++)       // cell 
			{	
				System.out.print("*");
			}	
         	System.out.println();
		}
		

	}

}
